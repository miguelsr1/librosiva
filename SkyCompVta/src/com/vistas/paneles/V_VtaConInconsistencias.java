    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (!txtNRC.getText().isEmpty()) {
            String nrc = txtNRC.getText().
                    substring(0, txtNRC.getText().length() - 1).concat("-").
                    concat(txtNRC.getText().substring(txtNRC.getText().length() - 1));

            List<VentaContribuyente> lista = ServVtaContribuyente.getListadoVtasNRCInconsistente(nrc, anhoFiscal.getYear());
            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(AccesoAVtaPadre.vtaPadre,
                        "No se encontrarón datos con el nrc " + txtNRC.getText(),
                        "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                listaVtas.clear();
                listaVtas.addAll(lista);
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

