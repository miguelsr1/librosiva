    private void btoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoGuardarActionPerformed
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().rollback();
            entityManager.getTransaction().begin();
            List<com.entidades.VentaContribuyente> merged = new ArrayList<com.entidades.VentaContribuyente>(listaVtas.size());
            for (com.entidades.VentaContribuyente v : listaVtas) {
                merged.add(entityManager.merge(v));
            }
            listaVtas.clear();
            listaVtas.addAll(merged);
        } finally {
        }
    }//GEN-LAST:event_btoGuardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (VentaContribuyente ventaContribuyente : listaVtas) {
            ventaContribuyente.setCsvVtaC(false);
        }
        
        btoGuardarActionPerformed(evt);
    }//GEN-LAST:event_jButton1ActionPerformed

